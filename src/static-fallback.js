  function authenticate(email, password) {
    var normalized = normalizeEmail(email);
    var user = trainingUsers.find(function (item) {
      return item.email === normalized;
    });
    if (!user || password !== "Training123!") {
      return null;
    }
    return sessionUser(user);
  }

  function loadSession() {
    try {
      var raw = window.localStorage.getItem(SESSION_KEY);
      if (!raw) return null;
      var parsed = JSON.parse(raw);
      if (!parsed || !parsed.email || !parsed.role || !Array.isArray(parsed.accessibleRoles)) {
        return null;
      }
      var canonical = trainingUsers.find(function (item) {
        return item.email === normalizeEmail(parsed.email);
      });
      return canonical ? sessionUser(canonical) : null;
    } catch (error) {
      return null;
    }
  }

  function saveSession(user) {
    window.localStorage.setItem(SESSION_KEY, JSON.stringify(user));
  }

  function clearSession() {
    window.localStorage.removeItem(SESSION_KEY);
  }

  function roleToId(role) {
    return roleIdByLabel[role] || role;
  }

  function accessibleRoleLabels(user) {
    return user.accessibleRoles.map(function (roleId) {
      return roleLabelById[roleId];
    });
  }

  function canAccessRole(user, role) {
    return Boolean(user && user.accessibleRoles.indexOf(roleToId(role)) >= 0);
  }

  function defaultRoleForUser(user) {
    return roleLabelById[user.accessibleRoles[0] || user.role];
  }

  function activeRoleKey(user) {
    return PROGRESS_PREFIX + ":" + normalizeEmail(user.email) + ":active-role";
  }

  function getActiveRole(user) {
    try {
      var saved = window.localStorage.getItem(activeRoleKey(user));
      if (saved) {
        return saved;
      }
    } catch (error) {
      return defaultRoleForUser(user);
    }
    return defaultRoleForUser(user);
  }

  function setActiveRole(user, role) {
    if (!canAccessRole(user, role)) {
      return false;
    }
    window.localStorage.setItem(activeRoleKey(user), role);
    return true;
  }

  function progressKey(user, role) {
    if (!user || !role) {
      return LEGACY_STORAGE_KEY;
    }
    return PROGRESS_PREFIX + ":" + normalizeEmail(user.email) + ":" + roleToId(role) + ":progress";
  }

  function loadProgress(role) {
    try {
      var user = loadSession();
      var selectedRole = user ? role || getActiveRole(user) : null;
      var raw = window.localStorage.getItem(progressKey(user, selectedRole));
      if (!raw && user && selectedRole) {
        raw = window.localStorage.getItem(LEGACY_STORAGE_KEY);
      }
      var parsed = raw ? JSON.parse(raw) : {};
      return {
        selectedRole: selectedRole || parsed.selectedRole || null,
        completedModules: Array.isArray(parsed.completedModules) ? parsed.completedModules : [],
        quizScores: parsed.quizScores && typeof parsed.quizScores === "object" ? parsed.quizScores : {},
        finalQuizScore: parsed.finalQuizScore || null
      };
    } catch (error) {
      return { selectedRole: null, completedModules: [], quizScores: {}, finalQuizScore: null };
    }
  }

  function saveProgress(progress) {
    var user = loadSession();
    var selectedRole = progress.selectedRole || (user ? getActiveRole(user) : null);
    try {
      window.localStorage.setItem(
        progressKey(user, selectedRole),
        JSON.stringify({
          selectedRole: selectedRole,
          completedModules: progress.completedModules || [],
          quizScores: progress.quizScores || {},
          finalQuizScore: progress.finalQuizScore || null
        })
      );
    } catch (error) {
      return progress;
    }
    return {
      selectedRole: selectedRole,
      completedModules: progress.completedModules || [],
      quizScores: progress.quizScores || {},
      finalQuizScore: progress.finalQuizScore || null
    };
  }

  function getProgress() {
    var user = loadSession();
    return loadProgress(user ? getActiveRole(user) : null);
  }

  function updateProgress(updater) {
    var current = getProgress();
    return saveProgress(updater(current));
  }

