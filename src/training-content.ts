interface PracticalStepsGuide {
  screen: string;
  card: string;
  role: Role | "Любая выбранная роль";
  dataBlock: string;
  fields: string[];
  allowedRule: string;
  blockedRule: string;
  action: string;
  actionNote: string;
  resultStatus: string;
  systemMessage: string;
  trace: string;
  example: string;
}

function practicalSteps(guide: PracticalStepsGuide): string[] {
  return [
    `Откройте экран «${guide.screen}» и выберите карточку «${guide.card}». Название карточки находится в заголовке модуля, а роль ученика видна в бейдже справа в верхней панели.`,
    `В верхней части карточки проверьте поле «Роль». Для этого задания должна быть выбрана роль «${guide.role}». Если роль другая, вернитесь на экран «Выберите роль» и нажмите нужную роль.`,
    `В блоке «${guide.dataBlock}» прочитайте поля:\n- ${guide.fields.join("\n- ")}`,
    `Сравните статус с правилом: ${guide.allowedRule}`,
    `Если действие недоступно, используйте правило блокировки: ${guide.blockedRule}`,
    `Нажмите кнопку «${guide.action}». ${guide.actionNote}`,
    `После нажатия проверьте результат: статус должен стать «${guide.resultStatus}», а система должна показать сообщение «${guide.systemMessage}».`,
    `Откройте блок «Учебный след» или «История» внизу карточки и убедитесь, что появилась запись: «${guide.trace}».`,
    `Пример: ${guide.example}`
  ];
}

