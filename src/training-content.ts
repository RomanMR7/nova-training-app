export type Role =
  | "Администратор"
  | "Саппорт"
  | "Мерчант"
  | "Трейдер"
  | "Провайдер";

export interface QuizOption {
  id: string;
  text: string;
}

export interface QuizQuestion {
  id: string;
  question: string;
  options: QuizOption[];
  correctOptionId: string;
  explanation: string;
}

export interface ScenarioOption {
  id: string;
  label: string;
  feedback: string;
  correct: boolean;
}

export interface ScenarioStep {
  id: string;
  title: string;
  instruction: string;
  options: ScenarioOption[];
}

export interface Scenario {
  id: string;
  title: string;
  summary: string;
  role: Role;
  steps: ScenarioStep[];
}

export interface TrainingModule {
  id: string;
  title: string;
  roles: Role[];
  explanation: string;
  steps: string[];
  mistakes: string[];
  checklist: string[];
  quiz: QuizQuestion[];
  scenario?: Scenario;
}

